      SUBROUTINE CNPS (Y,X,C,N)
C-------------------------------------------------------------------------------
C  @(#)cnps.f	5.1 06/22/94
C-------------------------------------------------------------------------------
c**********************************
c  Modified 8903 [RJM] See CHANGES.DOC
c**********************************
      integer*2 n, i, k
      real*4 C(*), y, x, c, h1, h0, arg, h2
c
      IF (N-2) 3,4,4
   3  Y=C(1)
      RETURN
C
   4  ARG=X+X
      H1=0.0
      H0=0.0
C
      DO 5 I=1,N
         K=N-I
         H2=H1
         H1=H0
   5     H0=ARG*H1 -H2+C(K+1)
      Y=0.5*(C(1)-H2+H0)
      RETURN
      END


